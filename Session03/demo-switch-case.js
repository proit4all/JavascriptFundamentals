let number = 5;
switch (number) {
  case 1:
    console.log("ONE");
    break;
  case 2:
    console.log("TWO");
    break;
  case 3:
    console.log("THREE");
    break;
  default:
    console.log("Not number");
    break;
}

let food = "cake";
switch (food) {
  case "cake":
    console.log("I like cake");

  case "pizza":
    console.log("I like pizza");

  case "ice cream":
    console.log("I like ice cream");

  default:
    console.log("I like all food");
    break;
}
